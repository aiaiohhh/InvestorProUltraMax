// Mock Holdings
const createHolding = (
  id: string,
  assetId: string,
  quantity: number,
  averageCost: number,
  portfolioId: string
): Holding => {
  const asset = getAssetById(assetId)!;
  const currentValue = quantity * asset.price;
  const totalCost = quantity * averageCost;
  const profitLoss = currentValue - totalCost;
  const profitLossPercent = (profitLoss / totalCost) * 100;
  
  return {
    id,
    assetId,
    asset,
    quantity,
    averageCost,
    currentValue,
    totalCost,
    profitLoss,
    profitLossPercent,
    portfolioId,
  };
};

